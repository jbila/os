    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        OrdemServico ordemServico = new OrdemServico();
        ordemServico.setAvaria(txtAvaria.getText());
        ordemServico.setServico(txtServico.getText());
        ordemServico.setOperacao(OperacaoEnum.ENTRADA.toString());
        ordemServico.setValor(Double.parseDouble(txtValor.getText()));
        ordemServico.setEquipamento(txtEquipamento.getText());

        ordemServico.setStatus(cboStatus.getSelectedItem().toString());
        
        Cliente cliente=(Cliente) cboCliente.getSelectedItem();
        ordemServico.setCliente(cliente);
        Tecnico tecnico=(Tecnico)cboTecnico.getSelectedItem();
        ordemServico.setTecnico(tecnico);
        
        double iva = Double.parseDouble(txtValor.getText()) * 0.16;
        ordemServico.setIva(iva);
     

        try {

            if (!txtEquipamento.getText().isEmpty() && !txtServico.getText().isEmpty()) {
                try {
                    daoOrdemDeServico.save(ordemServico);
                } catch (SQLException ex) {
                    Logger.getLogger(FrmOs.class.getName()).log(Level.SEVERE, null, ex);
                }
                limpar();
                preencherTabela();
            } else {
                JOptionPane.showMessageDialog(btnSave, "Preencha os campos");
            }

        } catch (HeadlessException ex) {
            // TODO Auto-generated catch block

        }


    }//GEN-LAST:event_btnSaveActionPerformed

