    /*---------limpar campos----------*/
    private void limpar() {
        txtAvaria.setText("");
        txtEquipamento.setText("");
        txtServico.setText("");
        txtServico.setText("");
        txtValor.setText("");
        txtId.setText("");

    }

    // preecher tabela
    private void preencherTabela() {

        DefaultTableModel model = (DefaultTableModel) table.getModel();
        Object[] row = new Object[9];
        model.setRowCount(0);// limpar a tabela
        List<OrdemServico> o = daoOrdemDeServico.findAll();
        for (int i = 0; i < o.size(); i++) {
            row[0] = o.get(i).getId();
            row[1] = o.get(i).getEquipamento();
            row[2] = o.get(i).getAvaria();
            row[3] = o.get(i).getServico();
            row[4] = o.get(i).getValor();
            row[5] = o.get(i).getStatus();
            row[6] = o.get(i).getCliente();
            row[7] = o.get(i).getTecnico();
             row[8] = o.get(i).getOperacao();
            model.addRow(row);
        }

    }

    private void fillTecnico() {
         List<Tecnico> tecnicos=daoTecnico.findAll();
        for (Tecnico tecnico : tecnicos) {
            cboTecnico.addItem(tecnico);
        }

    }

    private void fillCliente() {
        List<Cliente> clientes=daoCliente.findAll();
        for (Cliente cliente : clientes) {
            cboCliente.addItem(cliente);
        }
    }

    private void selection() {
        try {
            int selectedRow = table.getSelectedRow();
            if (selectedRow >= 0) {

                Object id = table.getValueAt(selectedRow, 0);
                Object equipamento = table.getValueAt(selectedRow, 1);
                Object avaria = table.getValueAt(selectedRow, 2);
                Object servico = table.getValueAt(selectedRow, 3);
                Object valor = table.getValueAt(selectedRow, 4);
                Object status = table.getValueAt(selectedRow, 5);
                Object cliente = table.getValueAt(selectedRow, 6);
                Object tecnico = table.getValueAt(selectedRow, 7);

                txtId.setText(id.toString());
                txtEquipamento.setText(equipamento.toString());
                txtAvaria.setText(avaria.toString());
                txtServico.setText(servico.toString());
                cboStatus.addItem(cboStatus.toString());
                txtValor.setText(valor.toString());
                cboTecnico.addItem(tecnico.toString());
                cboStatus.addItem(status.toString());
                cboCliente.addItem(cliente.toString());

            }
        } catch (NullPointerException ex) {

        }

    }
