    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String estadoActual = cboStatus.getSelectedItem().toString();
        double somatorioValor = 0.0;
        double somatorioIva = 0.0;
        int linha = 0;
        if (!estadoActual.isEmpty()) {
            List<OrdemServico> orders = daoOrdemDeServico.findAllByStatus(estadoActual);

            showInfo(orders);
            for (OrdemServico order : orders) {
                if (order.getValor() > 0) {
                    linha++;
                    somatorioIva = somatorioIva + order.getIva();
                    somatorioValor = somatorioValor + order.getValor();
                }
            }
        }
//System.out.print("IVA: "+somatorioIva+" Valor: "  +somatorioValor);
        lblSomatorioValor.setText("Somatorio: " + somatorioValor);
        lblSomatorioIva.setText("Iva: " + somatorioIva);
        lblLinha.setText("Total: " + linha);
    }//GEN-LAST:event_btnSearchActionPerformed

