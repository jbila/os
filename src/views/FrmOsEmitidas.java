    private void preencherTabela() {
        List<OrdemServico> ordens = daoOrdemDeServico.findAll();
        showInfo(ordens);

    }

    private void showInfo(List<OrdemServico> o) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        Object[] row = new Object[10];
        model.setRowCount(0);// limpar a tabela
        for (int i = 0; i < o.size(); i++) {
            row[0] = o.get(i).getId();
            row[1] = o.get(i).getEquipamento();
            row[2] = o.get(i).getAvaria();
            row[3] = o.get(i).getServico();
            row[4] = o.get(i).getValor();
            row[5] = o.get(i).getStatus();
            row[6] = o.get(i).getCliente();
            row[7] = o.get(i).getTecnico();
            row[8] = o.get(i).getData().toString();
                row[9] = o.get(i).getOperacao();
            model.addRow(row);
        }

    }

    private void selection() {
        try {
            int selectedRow = table.getSelectedRow();
            if (selectedRow >= 0) {

                Object id = table.getValueAt(selectedRow, 0);
                Object equipamento = table.getValueAt(selectedRow, 1);
                Object avaria = table.getValueAt(selectedRow, 2);
                Object servico = table.getValueAt(selectedRow, 3);
                Object valor = table.getValueAt(selectedRow, 4);
                Object status = table.getValueAt(selectedRow, 5);
                Object cliente = table.getValueAt(selectedRow, 6);
                Object tecnico = table.getValueAt(selectedRow, 7);

            }
        } catch (NullPointerException ex) {

        }

    }
