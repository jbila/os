    private void btnEntregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntregarActionPerformed

        OrdemServico ordemServico = daoOrdemDeServico.findById(Integer.parseInt(lblId.getText()));

        if (ordemServico != null) {

            if (ordemServico.getStatus().equals("PAGO")) {
                try {
                    ordemServico.setOperacao(OperacaoEnum.SAIDA.toString());
                    ordemServico.setCliente(ordemServico.getCliente());
                    ordemServico.setTecnico(  ordemServico.getTecnico());
                    daoOrdemDeServico.update(ordemServico);
                    preencherTabela();
                } catch (SQLException ex) {
                    Logger.getLogger(FrmOsSaida.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "FAÃ‡A O PAGAMENTO POR FAVOR!");
            }
        }
        else{
                            JOptionPane.showMessageDialog(rootPane, "Vazio");

        }


    }//GEN-LAST:event_btnEntregarActionPerformed

